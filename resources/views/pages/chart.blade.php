<h4><p class="opacity" style="color:#050930;">At STIMU, three working groups were created to develop standards. One was to develop
standards for programme content, delivery and assessment, including pertinent digital
learning content and delivery. This group was placed under the directions of the Academic
Board. Another one was to develop standards on management programme support which
include out- of-campus learning activities. This group was placed under the direction of the
Executive Management Group. The third working group was tasked to develop the
standards for following up and assessment of the results and outcome for applicability,
effectiveness and relevance. This last group was a joint effort of the Academic Board and the EMG and was placed under the direction of the BOD Committee on Quality
Assurance. The working group eventually evolved into the Standard and Quality
Assurance Unit with the working groups merged into one SQA Committee.
This section details the corresponding structure, responsibilities, procedures, processes
and resources for monitoring and reviewing of the University’s set of standards against the
set requirements.</p></h4>



