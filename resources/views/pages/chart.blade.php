  <h4><p style="color:#050930;"><b>Responsibilities of the BOD</b></p></h4> 
                     
   <h4><p class="opacity" style="color:#050930;">The highest governing structure of STIMU is the Board of Directors. The Board of Directors provides strategic leadership in STIMU’s governance. The Board oversees the functioning of the academic and corporate governance framework to enable the university to conduct its business in a way that foster long-term success, sustain competitiveness, and create value for its constituents and stakeholders. As the University is growing and contains relatively few academic faculties, some members of the BOD also act as the executive governing body of the University with particular managerial responsibilities to oversee the conduct of University business and provide advisory capacity. It approves all major developments and receives regular reports from the Academic Board and the Executive Management Group. The Board has the final decision on matters that impact on the University.</p></h4>


   <h4><p class="opacity" style="color:#050930;">The Board conducts annual strategic planning session where the university’s performance,
achievements and financial standing are reviewed and fed into new courses of actions for
succeeding medium and long term plans. It reviews the corporate performance and sets the
school’s policies and overall strategic direction. The Mission and Vision guide the strategic
direction, major transactions and management’s implementation plans set by the Board. The
Board conducts a regular monthly meeting. It also meets monthly with the Academic Board
and the Executive Management Group.</p></h4>

