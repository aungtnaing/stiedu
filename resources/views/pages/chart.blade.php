<h3><p style="color:#050930;"><b>The Executive Management Board (EMB)</b></p></h3> 
<h4><p class="opacity" style="color:#050930;">The Executive Management Board serves as the executive governing body of the university.
It is responsible for matters relating to the management and finance of the University. The
EMB reports to the BOD on matters concerning the overall management of the university;
while the Academic Board is delegated with the decision making on matters pertaining to the
academic status of the university. The BOD delegates the overall decision-making and
management to the EMB. EMB ensures the effective leadership, management and co-
ordination and implementation of all the major academic and operations activities. It is also
tasked to guide internal monitoring and review such as the financial state of the university,
programme delivery, as well as external events such as the state of the economy and politics
that will impact on the university.</p></h4>


