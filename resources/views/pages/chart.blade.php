<h3><p style="color:#050930;"><b>The Standards Quality Assurance Department (SQA)</b></p></h3> 
<h4><p class="opacity" style="color:#050930;">The SQA Department of STIMU consists of the SQA Committee and three sub units, the
Monitoring Team, Review and Planning Team and the Documentation and Public Relations
Team. SQA is under the direction of the University President. The Units work in close
coordination and collaboration with the University’s academic team and the Academic Board,
which together look carefully on the institution’s current and evolving standards.</p></h4>


<h4><p class="opacity" style="color:#050930;">Standards and procedures are set by each of the five board advisory Committee
Chairpersons who establishes evaluative criteria and procedures for all teachers and
management support staff. The University takes into account the Ministry quality code on
Matriculation as well as standards set by its international partners in setting up the standards,
implementation and maintenance procedures. This is amended from time to time in
collaboration with the Academic Board Committee on Quality Assurance and learning
teaching and assessment Committee.</p></h4>


<h4><p class="opacity" style="color:#050930;">The standards adopted by STIMU conform to those expected by their collaborative partners,
in addition to those which they have developed themselves. A preliminary mapping exercise
undertaken by Faculty staff at the University of Bedfordshire in early 2015 confirms that levels
and standards of academic work at STIMU are broadly in line with those within the University
of Bedfordshire.</p></h4>



<h4><p class="opacity" style="color:#050930;">STIMU is committed to ensuring that students have a high quality learning experience, and
that the standards of qualifications are set at appropriate levels. Programme proposals are
first approved at school level, so as to ensure that the proposed programmes are compatible
with school plans, and that only sound proposals are put forward for University approval.</p></h4>


<h4><p class="opacity" style="color:#050930;">Establishing that programme standards are appropriate, and that the quality of the student
experience can be assured are the principal concerns of programme approval process were
an appropriately qualified internal and external panel examines the programme.</p></h4>


<h4><p class="opacity" style="color:#050930;">Student’s achievement is monitored in a number of ways including consideration at
assessment boards, as part of the quality monitoring process, and by the Committee of
Monitoring and Audit who decide on the awards students are given.</p></h4>



<h4><p class="opacity" style="color:#050930;">At this time we have not adopted the UK method of external examiners as this is not well
developed outside of the UK.</p></h4>


<h4><p class="opacity" style="color:#050930;">The University also surveys students on a regular basis to provide feedback as they provide
